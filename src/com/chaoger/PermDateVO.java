package com.chaoger;

public class PermDateVO {
    private String userId;
    private String modelName;
    private String demensionKey;
    private String columnFields;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getModelName() {
        return modelName;
    }

    public void setModelName(String modelName) {
        this.modelName = modelName;
    }

    public String getDemensionKey() {
        return demensionKey;
    }

    public void setDemensionKey(String demensionKey) {
        this.demensionKey = demensionKey;
    }

    public String getColumnFields() {
        return columnFields;
    }

    public void setColumnFields(String columnFields) {
        this.columnFields = columnFields;
    }
}
